//! # export
//!
//! 3D export pipeline. Mirrors `periodica.export.*`:
//! - `export_glsl` — GLSL 450+ shader (NEW — PlayTowEngine primary)
//! - `export_hlsl` — DirectX HLSL (legacy parity)
//! - `export_sdf_raw` — raw float32 voxel volume + JSON header
//! - `export_vtk_legacy` — ASCII VTK STRUCTURED_POINTS
//! - `export_stl` — binary/ASCII STL surface mesh from voxel boundary quads
//! - `export_obj` — OBJ + MTL with per-phase materials
